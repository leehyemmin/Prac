package ex09;

public class Str01 {

	public static void main(String[] args) {

		String str = "";
		
		// 문자열이 결합될때마다 새로운 메모리가 생성.(성능에 안좋음)
		for(int i=0;i<100;i++) {
			str += i; 
		}
		
		System.out.println(str);
	}

}


